export function useTranslation() {
    const { settings } = useSettings();
    const lang = settings.language;

    const t = (key: string): string => {
        if (!translations[key]) {
            console.warn(`Translation key not found: ${key}`);
            return key;
        }
        return translations[key][lang] || translations[key].en;
    };

    return { t, lang };
}
